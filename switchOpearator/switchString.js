let day = "d";
switch(day)
{
    case "a":
        console.log("this is monday");
        break;
    case "b":
        console.log("this is tuesday");
        break;
    case "c":
        console.log("this is wednesday");
        break;
    case "d":
        console.log("this is wednesday");
        break;
    case "e":
        console.log("this is thursday");
        break;
    case "f":
        console.log("this is friday");
        break;
    case "g":
        console.log("this is saturday");
        break;
    default:
        console.log("invalid day");
        break;
}